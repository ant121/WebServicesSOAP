    private static int calculo(int dia1, int dia2, int dia3, int dia4, int dia5, int dia6) {
        uddi.ServicioDGIP_Service service = new uddi.ServicioDGIP_Service();
        uddi.ServicioDGIP port = service.getServicioDGIPPort();
        return port.calculo(dia1, dia2, dia3, dia4, dia5, dia6);
    }

    private static double calculosueldo(double pagoH) {
        uddi.ServicioDGIP_Service service = new uddi.ServicioDGIP_Service();
        uddi.ServicioDGIP port = service.getServicioDGIPPort();
        return port.calculosueldo(pagoH);
    }
